static ODR_t myVar_OD_callback(OD_stream_t *stream, void *buf,
                                OD_size_t count, OD_size_t *countWritten)
{
	uint32_t oldVal = 0;
	if (stream->dataLength <= sizeof(uint32_t)) {
	        memcpy(&oldVal, stream->dataOrig, stream->dataLength);
	    }

    /* Call the default write function first to actually store the value */
    ODR_t result = OD_writeOriginal(stream, buf, count, countWritten);

    if (result == ODR_OK) {
        /* Read the new value */
        uint32_t newValue = 0;
        memcpy(&newValue, stream->dataOrig, sizeof(newValue));

        /* Your logic here */
        printf("OD variable changed: 0x%04X sub%d, oldValue = %lu, newValue = %lu\r\n",
               stream->index,
               stream->subIndex,
			   oldVal,
               newValue);
    }

    return result;
}

void myApp_init(OD_t *od)
{
    /* Find the OD entry by index, e.g. 0x6000 */
    OD_entry_t *entry = OD_find(od, 0x6000);
    if (entry == NULL) return;

    /* Initialize extension with your write callback */
    memset(myVar_extension.flagsPDO, 0, sizeof(myVar_extension.flagsPDO));
    myVar_extension.read         = OD_readOriginal;  /* keep default read */
    myVar_extension.write        = myVar_OD_callback; /* your custom write */
    myVar_extension.object       = NULL; /* optional user pointer */

    OD_extension_init(entry, &myVar_extension);
}

